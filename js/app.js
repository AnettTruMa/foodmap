/*
FUNCION DE JS LLAMADA GET_ONLY DONDE SE RECIBE UN PARAMETRO EN FORMA
DE ARREGLO, ESTO PODRIA DECIRSE QUE SON LOS CHECKBOX O LISTA QUE
EL USUARIO QUIERE FILTRAR PROVENIENTE DEL UNIVERSO FILTROS
SE CREA UNA VARIABLE filtroParaImprimir QUE ES UN ARREGLO VACIO,
ESTE NOS SERVIRA PARA RETORNAR AL USUARIO LOS RESTAURANTES QUE CUMPLEN
CON LOS CRITERIOS DE FILTRADO
*/
function get_only(paraFiltrar){
  var filtroParaImprimir=[];//INICIALIZAR VARIABLE

/*
SE CREA EL PRIMER BUCLE PARA RECORRER TODOS LOS RESTAURANTES,
CARA ITERACIÓN SE ALMACENA EN EL PARAMETRO RESTAURANT.

EL SEGUNDO BUBLE ANIDADO ES PARA RECORRER (paraFiltrar)
LO QUE EL USUARIO ESTA FILRANDO O LO UNICO QUE QUIERE MOSTRAR
CADA ITERACIÓN ALMACENA LAS POSICIONES DEL ARREGLO DEAL
*/
  nuevoArr.forEach(function(restaurant){
    //console.log(restaurant['types'])
    //ARREGLO TEMPORAL YA QUE DENTRO DE CADA RESTAURANT ESTA EL OBJETO TYPES
    // Y SE DEBE HACER LA COMPARATIVA SI CUMPLE CON EL CRITERIO
    var temp_types=restaurant['types'];
    var cumple=false;
    temp_types.forEach(function(cadatipo){
      paraFiltrar.forEach(function(filtro){
        if(cadatipo==filtro){
          cumple=true;
          return false;
        }
      });
      if(cumple)
        filtroParaImprimir.push(restaurant);
      cumple=false;
    });
  });
  /*
  LINEA 62 => SI SE CUMPLE LA CONDICIÓN DE QUE UN RESTAURANT
  TIENE DENTRO DE SU PROPIEDAD DEAL EL VALOR QUE EL USUARIO
  ESTA FILTRANDO SE VA A CONCATENAR, ES DECIR, A SUMAR AL
  NUEVO ARREGLO QUE RETORNA LA FUNCION QUE REALIZA EL FILTRADO
  ESTA ITERACIÓN SE REPITE N NUMERO DE VECES, EN ESTE EJEMPLO 10 VECES
  YA QUE SON 5 RESTAURANTES QUE BUSCA QUE CUMPLAN CON EL FILTRO A Y B
  */
  return (filtroParaImprimir);//RETORNO
}


